                        <strong><span style="font-size: 20px;font-family:Cambria, Cochin, Georgia, Times, 'Times New Roman', serif;">What is CORONAVIRUS?</span></strong>
                        <p style="font-family: Cambria, Cochin, Georgia, Times, 'Times New Roman', serif;">The coronavirus is a family of viruses that can cause a range of illnesses in humans including common cold and more severe forms like SARS and MERS which are life-threatening. The virus is named after its shape which takes the form of a crown with protrusions around it and hence is known as coronavirus.</p>
                        <p style="font-family: Cambria, Cochin, Georgia, Times, 'Times New Roman', serif;">Researchers first identified a coronavirus in 1937, isolating one that was responsible for a type of bronchitis in birds that had the potential to devastate poultry stocks.

Scientists found evidence of human coronaviruses in the 1960s, in the noses of people with the common cold.

Human coronaviruses that are particularly prevalent include 229E, NL63, OC43, and HKU1.

The name “coronavirus” comes from the crown-like projections on their surfaces. “Corona” in Latin means “halo” or “crown.”

Among humans, coronavirus infections most often occur during the winter months and early spring.</p>
